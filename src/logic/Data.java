package logic;

public class Data {
	
	private int day; 
	private double value; 
	
	public Data(int day, double value) {
		super();
		this.day = day;
		this.value = value;
	}
	
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public double getValue() {
		return value;
	}
	public void setValue(double value) {
		this.value = value;
	}
	
	

}
